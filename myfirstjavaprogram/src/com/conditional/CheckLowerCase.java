//Write a Java program to check whether a character is lowercase (a–z).

package com.conditional;

public class CheckLowerCase {

	public static void main(String[] args) {
		char ch = 'a';
		if (ch >= 'a' && ch <= 'z') {
			System.out.println("The given character is in lowercase ");
		}

	}

}
