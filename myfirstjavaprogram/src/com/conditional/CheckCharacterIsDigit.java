//Write a Java program to check whether a given character is a digit (0–9)

package com.conditional;

public class CheckCharacterIsDigit {

	public static void main(String[] args) {
		char ch = '5';
		if (ch >= '0' && ch <= '9') {
			System.out.println("Given character is digit!!");
		}

	}

}
