
//WAP to swap numbers 
package com.javapractice;

public class Swapping2No {

	public static void main(String[] args) {
		int a = 20;
		int b = 30;
		// store a value in temp
		System.out.println("Before swapping numbers a is: " + a + " and b is:" + b);
		int temp = a;
		a = b;
		b = temp;
		System.out.println("After swapping numbers a is:" + a + " and b is " + b);

	}

}
